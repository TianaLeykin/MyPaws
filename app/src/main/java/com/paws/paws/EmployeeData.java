package com.paws.paws;

public class EmployeeData {
    public String name;
    public String phone;
    public String email;
    public int employee_icon;

    EmployeeData(String name, String phone, String email, int employee_icon) {
        this.name = name;
        this.phone = phone;
        this.email = email;
        this.employee_icon = employee_icon;
    }

}
